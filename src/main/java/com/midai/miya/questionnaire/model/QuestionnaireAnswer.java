package com.midai.miya.questionnaire.model;

import java.io.Serializable;
import java.util.Date;
public class QuestionnaireAnswer implements Serializable {

    private static final long serialVersionUID = 1L;
    /**
     * 调查问卷用户回答ID
     */
    private String questionnaireAnswerId;
    /**
     * 调查问卷问题ID
     */
    private String questionnaireQuestionId;
    /**
     * 调查问卷用户回答创建时间
     */
    private Date questionnaireQuestionCreateTime;
    /**
     * 用户ID 如果用户登录了就插入，没有就为空
     */
    private String userId;
    /**
     * 选项id
     */
    private String questionnaireOptionId;
    public String getQuestionnaireAnswerId(){
        return questionnaireAnswerId;
    }
    public void setQuestionnaireAnswerId(String questionnaireAnswerId){
        this.questionnaireAnswerId=questionnaireAnswerId;
    }
    public String getQuestionnaireQuestionId(){
        return questionnaireQuestionId;
    }
    public void setQuestionnaireQuestionId(String questionnaireQuestionId){
        this.questionnaireQuestionId=questionnaireQuestionId;
    }
    public Date getQuestionnaireQuestionCreateTime(){
        return questionnaireQuestionCreateTime;
    }
    public void setQuestionnaireQuestionCreateTime(Date questionnaireQuestionCreateTime){
        this.questionnaireQuestionCreateTime=questionnaireQuestionCreateTime;
    }
    public String getUserId(){
        return userId;
    }
    public void setUserId(String userId){
        this.userId=userId;
    }
	public String getQuestionnaireOptionId() {
		return questionnaireOptionId;
	}
	public void setQuestionnaireOptionId(String questionnaireOptionId) {
		this.questionnaireOptionId = questionnaireOptionId;
	}
}