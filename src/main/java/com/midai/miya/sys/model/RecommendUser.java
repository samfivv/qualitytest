package com.midai.miya.sys.model;

import java.io.Serializable;
public class RecommendUser implements Serializable {

    private static final long serialVersionUID = 1L;
    /**
     * 推荐Id
     */
    private String recommendUserId;
    /**
     * 用户Id
     */
    private String userId;
    /**
     * 类型 1 推荐机构 2 推荐特长人
     */
    private Integer recommendType;
    /**
     * 排序
     */
    private Integer recommendSort;
    /**
     * 推荐图片
     */
    private String recommendImgUrl;
    /**
     * 推荐理由
     */
    private String recommendDesc;
    /**
     * 推荐位置 1首页 2达人首页
     */
    private Integer recommendLocation;
    
    private String userNickname;
    private String userSignature;
    private String userMail;
    public String getRecommendUserId(){
        return recommendUserId;
    }
    public void setRecommendUserId(String recommendUserId){
        this.recommendUserId=recommendUserId;
    }
    public String getUserId(){
        return userId;
    }
    public void setUserId(String userId){
        this.userId=userId;
    }
    public Integer getRecommendType(){
        return recommendType;
    }
    public void setRecommendType(Integer recommendType){
        this.recommendType=recommendType;
    }
    public Integer getRecommendSort(){
        return recommendSort;
    }
    public void setRecommendSort(Integer recommendSort){
        this.recommendSort=recommendSort;
    }
	public String getUserNickname() {
		return userNickname;
	}
	public void setUserNickname(String userNickname) {
		this.userNickname = userNickname;
	}
	public String getUserSignature() {
		return userSignature;
	}
	public void setUserSignature(String userSignature) {
		this.userSignature = userSignature;
	}
	public String getUserMail() {
		return userMail;
	}
	public void setUserMail(String userMail) {
		this.userMail = userMail;
	}
	public String getRecommendImgUrl() {
		return recommendImgUrl;
	}
	public void setRecommendImgUrl(String recommendImgUrl) {
		this.recommendImgUrl = recommendImgUrl;
	}
	public String getRecommendDesc() {
		return recommendDesc;
	}
	public void setRecommendDesc(String recommendDesc) {
		this.recommendDesc = recommendDesc;
	}
	public Integer getRecommendLocation() {
		return recommendLocation;
	}
	public void setRecommendLocation(Integer recommendLocation) {
		this.recommendLocation = recommendLocation;
	}
}