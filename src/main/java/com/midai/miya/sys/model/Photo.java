package com.midai.miya.sys.model;

import java.util.Date;
import java.io.Serializable;
public class Photo implements Serializable {

    private static final long serialVersionUID = 1L;
    /**
     * 相片Id
     */
    private String photoId;
    /**
     * 相片名称
     */
    private String photoName;
    /**
     * 相片描述
     */
    private String photoDesc;
    /**
     * 相片保存路径
     */
    private String photoPath;
    /**
     * 上传时间
     */
    private Date photoCreateTime;
    /**
     * 相册Id
     */
    private String photoAlbumId;
    /**
     * 状态
     */
    private Integer photoState;
    
    private String userId;
    
    private String userNickname;
    public String getPhotoId(){
        return photoId;
    }
    public void setPhotoId(String photoId){
        this.photoId=photoId;
    }
    public String getPhotoName(){
        return photoName;
    }
    public void setPhotoName(String photoName){
        this.photoName=photoName;
    }
    public String getPhotoDesc(){
        return photoDesc;
    }
    public void setPhotoDesc(String photoDesc){
        this.photoDesc=photoDesc;
    }
    public String getPhotoPath(){
        return photoPath;
    }
    public void setPhotoPath(String photoPath){
        this.photoPath=photoPath;
    }
    public Date getPhotoCreateTime(){
        return photoCreateTime;
    }
    public void setPhotoCreateTime(Date photoCreateTime){
        this.photoCreateTime=photoCreateTime;
    }
    public String getPhotoAlbumId(){
        return photoAlbumId;
    }
    public void setPhotoAlbumId(String photoAlbumId){
        this.photoAlbumId=photoAlbumId;
    }
	public Integer getPhotoState() {
		return photoState;
	}
	public void setPhotoState(Integer photoState) {
		this.photoState = photoState;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserNickname() {
		return userNickname;
	}
	public void setUserNickname(String userNickname) {
		this.userNickname = userNickname;
	}
}