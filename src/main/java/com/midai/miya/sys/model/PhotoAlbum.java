package com.midai.miya.sys.model;

import java.util.Date;
import java.io.Serializable;
public class PhotoAlbum implements Serializable {

    private static final long serialVersionUID = 1L;
    /**
     * 
     */
    private String userId;
    /**
     * 相册Id
     */
    private String photoAlbumId;
    /**
     * 相册名称
     */
    private String photoAlbumName;
    /**
     * 相册描述
     */
    private String photoAlbumDesc;
    /**
     * 权限 1公开相册  3私密相册
     */
    private Integer photoAlbumLimits;
    /**
     * 创建时间
     */
    private Date photoAlbumCreateTime;
    /**
     * 照片数量
     */
    private Integer photoAlbumCount;
    /**
     * 封面图片
     */
    private String photoId;
    public String getUserId(){
        return userId;
    }
    public void setUserId(String userId){
        this.userId=userId;
    }
    public String getPhotoAlbumId(){
        return photoAlbumId;
    }
    public void setPhotoAlbumId(String photoAlbumId){
        this.photoAlbumId=photoAlbumId;
    }
    public String getPhotoAlbumName(){
        return photoAlbumName;
    }
    public void setPhotoAlbumName(String photoAlbumName){
        this.photoAlbumName=photoAlbumName;
    }
    public String getPhotoAlbumDesc(){
        return photoAlbumDesc;
    }
    public void setPhotoAlbumDesc(String photoAlbumDesc){
        this.photoAlbumDesc=photoAlbumDesc;
    }
    public Integer getPhotoAlbumLimits(){
        return photoAlbumLimits;
    }
    public void setPhotoAlbumLimits(Integer photoAlbumLimits){
        this.photoAlbumLimits=photoAlbumLimits;
    }
    public Date getPhotoAlbumCreateTime(){
        return photoAlbumCreateTime;
    }
    public void setPhotoAlbumCreateTime(Date photoAlbumCreateTime){
        this.photoAlbumCreateTime=photoAlbumCreateTime;
    }
    public Integer getPhotoAlbumCount(){
        return photoAlbumCount;
    }
    public void setPhotoAlbumCount(Integer photoAlbumCount){
        this.photoAlbumCount=photoAlbumCount;
    }
    public String getPhotoId(){
        return photoId;
    }
    public void setPhotoId(String photoId){
        this.photoId=photoId;
    }
}