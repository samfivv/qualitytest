package com.midai.miya.sys.model;

import java.io.Serializable;
public class Copywriter implements Serializable {

    private static final long serialVersionUID = 1L;
    /**
     * 文案ID
     */
    private String copywriterId;
    /**
     * 文案跳转链接
     */
    private String copywriterUrl;
    /**
     * 文案描述
     */
    private String copywriterDesc;
    /**
     * 文案图片链接
     */
    private String copywriterImgUrl;
    /**
     * 文案顺序
     */
    private Integer copywriterSort;
    /**
     * 版本
     * 王梦圆
     * 2015年7月21日
     */
    private String copywriterVersion;
    /**
     * 文案类型
     */
    private Integer copywriterType;
    private String copywroterTypeStr;
    /**
     * 兴趣Id
     */
    private String copywriterInterestId;
    /**
     * 页面
     */
    private Integer copywriterPage;
    public String getCopywriterId(){
        return copywriterId;
    }
    public void setCopywriterId(String copywriterId){
        this.copywriterId=copywriterId;
    }
    public String getCopywriterUrl(){
        return copywriterUrl;
    }
    public void setCopywriterUrl(String copywriterUrl){
        this.copywriterUrl=copywriterUrl;
    }
    public String getCopywriterDesc(){
        return copywriterDesc;
    }
    public void setCopywriterDesc(String copywriterDesc){
        this.copywriterDesc=copywriterDesc;
    }
    public String getCopywriterImgUrl(){
        return copywriterImgUrl;
    }
    public void setCopywriterImgUrl(String copywriterImgUrl){
        this.copywriterImgUrl=copywriterImgUrl;
    }
    public Integer getCopywriterSort(){
        return copywriterSort;
    }
    public void setCopywriterSort(Integer copywriterSort){
        this.copywriterSort=copywriterSort;
    }
	public String getCopywriterVersion() {
		return copywriterVersion;
	}
	public void setCopywriterVersion(String copywriterVersion) {
		this.copywriterVersion = copywriterVersion;
	}
	public Integer getCopywriterType() {
		return copywriterType;
	}
	public void setCopywriterType(Integer copywriterType) {
		this.copywriterType = copywriterType;
	}
	public String getCopywriterInterestId() {
		return copywriterInterestId;
	}
	public void setCopywriterInterestId(String copywriterInterestId) {
		this.copywriterInterestId = copywriterInterestId;
	}
	public String getCopywroterTypeStr() {
		if(copywriterType==1){
			return "其他链接";
		}else{
			return "内部专辑";
		}
	}
	public void setCopywroterTypeStr(String copywroterTypeStr) {
		this.copywroterTypeStr = copywroterTypeStr;
	}
	public Integer getCopywriterPage() {
		return copywriterPage;
	}
	public void setCopywriterPage(Integer copywriterPage) {
		this.copywriterPage = copywriterPage;
	}
}